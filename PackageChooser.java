    private void addHotelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addHotelActionPerformed
        String comb = combohot.getSelectedItem().toString();
        //System.out.println(comb);
        try {
            ResultSet rs;
            String sql2="SELECT `package` FROM `hotel` WHERE `hotel_id`= \""+comb+"\"";
            pst = conn.prepareStatement(sql2);
            rs = pst.executeQuery();
            rs.next();
            
            String sql = "INSERT INTO `cart` VALUES(?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, combohot.getSelectedItem().toString());
            pst.setInt(2,rs.getInt(1));
            pst.executeUpdate();
        } catch (Exception e) {
            System.out.println(e);
        }
        
        
    }//GEN-LAST:event_addHotelActionPerformed

